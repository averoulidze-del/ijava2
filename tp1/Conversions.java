class Conversions extends Program {

    void algorithm(){

      print( "(int) 4.6 ->" );
      println( (int) 4.6 ); // =>4

      print( "(double) 4 ->" );
      println( (double) 4 );// =>4.0

      print("2.1 + 3 -> ");
      println(2.1 + 3);// =>5

      print("(int) 'A' -> ");
      println( (int) 'A' );// =>65

      print(" (char) 66 -> ");
      println( (char) 66);// =>B


      print(" (int) 3.7 * 2 -> ");
      println( (int) 3.7 * 2);// =>6

      print("(int) (3.7 * 2) -> ");
      println((int) (3.7 * 2));// =>7

      print(" \"ABC\" + (char) 65 ");
      println( "ABC" + (char) 65 );// =>ABCA

    }

}
